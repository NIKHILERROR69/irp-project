float CtoF(float celcius)
{
    return (celcius * 9 / 5) + 32;
}
float FtoC(float fahrenheit)
{
    return (fahrenheit - 32) * 5 / 9;
}
float CtoK(float celcius)
{
    return celcius + 273.15;
}
float KtoC(float kelvin)
{
    return kelvin - 273.15;
}
